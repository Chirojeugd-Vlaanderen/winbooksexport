/**
 * Implements hook_civicrm_preProcess().
 *
 * @link http://wiki.civicrm.org/confluence/display/CRMDOC/hook_civicrm_preProcess
 */
function winbooksexport_civicrm_preProcess($formName, &$form) {
  if ($formName == "CRM_Financial_Form_Export") {
    $form->export_format = "Winbooks";
  }
}

function winbooksexport_civicrm_buildForm($formName, &$form) {
  if ($formName == "CRM_Financial_Form_Export") {
    // TODO: can't we just add Winbooks to the existing element?
    $form->removeElement('export_format');
    
    $optionTypes = array(
      'Winbooks' => ts('Export to Winbooks'),
    );

    $form->addRadio('export_format', NULL, $optionTypes, NULL, '<br/>', TRUE);
  }
}

